let snl = [];

snail = function(array) {
  if (array.length !== 0){
    // ****************************************************************************************************
    // TOP
    // ****************************************************************************************************
    snl = [...snl].concat([...array[0]]);
    array.shift()
    // ****************************************************************************************************
    // RIGHT
    // ****************************************************************************************************
    for(let x = 0; x < array.length; x++){
      snl.push(array[x][array[x].length - 1])
      array[x].pop();
    }
    // ****************************************************************************************************
    // BOTTOM
    // ****************************************************************************************************
    let lastArray = array[array.length - 1] !== undefined && array[array.length - 1];
    if(lastArray){
      for(let y = lastArray.length - 1; y >= 0; y--){
        snl.push(array[array.length - 1][y])
        array[array.length - 1].pop();
      }
    }
    array.pop()
    // ****************************************************************************************************
    // RIGHT
    // ****************************************************************************************************
    for(let z = array.length - 1; z > 0; z --){
      snl.push(array[z][0])
      array[z].shift()
    }
    return snail(array)
  } else {
    return snl;
  }
}

